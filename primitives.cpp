#include <iostream>
using namespace std;

int main() {
    // Declare and initialize variables
    int myInt = 10;
    float myFloat = 3.14;
    bool myBool = true;
    char myChar = 'A';

    // Print their values
    cout << "Integer: " << myInt << endl;
    cout << "Float: " << myFloat << endl;
    cout << "Boolean: " << myBool << endl;
    cout << "Character: " << myChar << endl;

    return 0;
}
